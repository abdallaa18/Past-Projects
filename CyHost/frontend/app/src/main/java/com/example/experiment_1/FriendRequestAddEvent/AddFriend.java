package com.example.experiment_1.FriendRequestAddEvent;

public class AddFriend {




    private String addFriendName;
    private String addFriendUserName;

    public AddFriend(String addFriendName, String addFriendUserName) {
        this.addFriendName = addFriendName;
        this.addFriendUserName = addFriendUserName;
    }

    public String getAddFriendName() {
        return addFriendName;
    }

    public String getAddFriendUserName() {
        return addFriendUserName;
    }
}
